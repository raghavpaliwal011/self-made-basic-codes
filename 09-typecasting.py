#str to int

l = "1000"
print (l)
print (type(l))
a = int(l)
print (a)
print (type(l))

#list to tuple 
my_l = [1,2,3,4,5,6,7,8,9]
print (my_l)
print (type(my_l))
new_my_l = tuple(my_l)
print (new_my_l)
print (type(new_my_l))

#int to float 
number = 100
print (number)
print (type(number))
new_number = float(number)
print (new_number)
print (type(new_number))

#list to set
lis = [1,2,3,4,5,6,7,8,9]
print (lis)
print (type(lis))
new_lis = set (lis)
print (new_lis)
print (type(new_lis))